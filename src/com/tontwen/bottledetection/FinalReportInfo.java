package com.tontwen.bottledetection;

public class FinalReportInfo {
	private String bottleDetectNumber;
	private String bottleNumber;
	private String carNumber;
	private String finalDetectResult;
	private String reportExaminer;
	private String reportChecker;
	private String finalDetectDate;
	private String bottleNextCheckDate;
	public String getBottleDetectNumber() {
		return bottleDetectNumber;
	}
	public void setBottleDetectNumber(String bottleDetectNumber) {
		this.bottleDetectNumber = bottleDetectNumber;
	}
	public String getBottleNumber() {
		return bottleNumber;
	}
	public void setBottleNumber(String bottleNumber) {
		this.bottleNumber = bottleNumber;
	}
	public String getCarNumber() {
		return carNumber;
	}
	public void setCarNumber(String carNumber) {
		this.carNumber = carNumber;
	}
	public String getFinalDetectResult() {
		return finalDetectResult;
	}
	public void setFinalDetectResult(String finalDetectResult) {
		this.finalDetectResult = finalDetectResult;
	}
	public String getReportExaminer() {
		return reportExaminer;
	}
	public void setReportExaminer(String reportExaminer) {
		this.reportExaminer = reportExaminer;
	}
	public String getReportChecker() {
		return reportChecker;
	}
	public void setReportChecker(String reportChecker) {
		this.reportChecker = reportChecker;
	}
	public String getFinalDetectDate() {
		return finalDetectDate;
	}
	public void setFinalDetectDate(String finalDetectDate) {
		this.finalDetectDate = finalDetectDate;
	}
	public String getBottleNextCheckDate() {
		return bottleNextCheckDate;
	}
	public void setBottleNextCheckDate(String bottleNextCheckDate) {
		this.bottleNextCheckDate = bottleNextCheckDate;
	}
	

}
