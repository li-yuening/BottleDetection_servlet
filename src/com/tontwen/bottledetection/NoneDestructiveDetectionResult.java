package com.tontwen.bottledetection;

public class NoneDestructiveDetectionResult {
	private String bottleDetectNumber;
	private String operatorName;
	private String noneDestructivePosition;
	private String noneDestructiveDetail;
	private String noneDestructiveResult;
	public String getBottleDetectNumber() {
		return bottleDetectNumber;
	}
	public void setBottleDetectNumber(String bottleDetectNumber) {
		this.bottleDetectNumber = bottleDetectNumber;
	}
	public String getOperatorName() {
		return operatorName;
	}
	public void setOperatorName(String operatorName) {
		this.operatorName = operatorName;
	}
	public String getNoneDestructiveResult() {
		return noneDestructiveResult;
	}
	public void setNoneDestructiveResult(String noneDestructiveResult) {
		this.noneDestructiveResult = noneDestructiveResult;
	}
	public String getNoneDestructiveDetail() {
		return noneDestructiveDetail;
	}
	public void setNoneDestructiveDetail(String noneDestructiveDetail) {
		this.noneDestructiveDetail = noneDestructiveDetail;
	}
	public String getNoneDestructivePosition() {
		return noneDestructivePosition;
	}
	public void setNoneDestructivePosition(String noneDestructivePosition) {
		this.noneDestructivePosition = noneDestructivePosition;
	}
	
	

}
