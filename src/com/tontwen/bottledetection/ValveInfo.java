package com.tontwen.bottledetection;

public class ValveInfo {
	private String valveType;
	private String valveTestPressure;
	private String valveTestMedium;
	private String valveAddOperator;
	private String valveAddTime;
	public String getValveType() {
		return valveType;
	}
	public void setValveType(String valveType) {
		this.valveType = valveType;
	}
	public String getValveTestPressure() {
		return valveTestPressure;
	}
	public void setValveTestPressure(String valveTestPressure) {
		this.valveTestPressure = valveTestPressure;
	}
	public String getValveTestMedium() {
		return valveTestMedium;
	}
	public void setValveTestMedium(String valveTestMedium) {
		this.valveTestMedium = valveTestMedium;
	}
	public String getValveAddOperator() {
		return valveAddOperator;
	}
	public void setValveAddOperator(String valveAddOperator) {
		this.valveAddOperator = valveAddOperator;
	}
	public String getValveAddTime() {
		return valveAddTime;
	}
	public void setValveAddTime(String valveAddTime) {
		this.valveAddTime = valveAddTime;
	}
	
}
