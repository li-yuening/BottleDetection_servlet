package com.tontwen.bottledetection;

public class VacuumInfo {
	private String bottleDetectNumber;
	private String bottleNumber;
	private String carNumber;
	private String vacuumPressure;
	private String vacuumResult;
	private String operatorName;
	private String yrs;
	public String getBottleDetectNumber() {
		return bottleDetectNumber;
	}
	public void setBottleDetectNumber(String bottleDetectNumber) {
		this.bottleDetectNumber = bottleDetectNumber;
	}
	public String getBottleNumber() {
		return bottleNumber;
	}
	public void setBottleNumber(String bottleNumber) {
		this.bottleNumber = bottleNumber;
	}
	public String getCarNumber() {
		return carNumber;
	}
	public void setCarNumber(String carNumber) {
		this.carNumber = carNumber;
	}
	public String getVacuumPressure() {
		return vacuumPressure;
	}
	public void setVacuumPressure(String vacuumPressure) {
		this.vacuumPressure = vacuumPressure;
	}
	public String getVacuumResult() {
		return vacuumResult;
	}
	public void setVacuumResult(String vacuumResult) {
		this.vacuumResult = vacuumResult;
	}
	public String getOperatorName() {
		return operatorName;
	}
	public void setOperatorName(String operatorName) {
		this.operatorName = operatorName;
	}
	public String getYrs() {
		return yrs;
	}
	public void setYrs(String yrs) {
		this.yrs = yrs;
	}
	
	
	
	

}
