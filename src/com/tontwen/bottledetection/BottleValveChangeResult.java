package com.tontwen.bottledetection;

public class BottleValveChangeResult {
	private String bottleDetectNumber;
	private String operatorName;
	private String bottleValveTestNo;
	private String bottleValveType;
	private String bottleValveTestPressure;
	private String bottleValveTestMedium;
	private String bottleValveChangeResult;
	public String getBottleDetectNumber() {
		return bottleDetectNumber;
	}
	public void setBottleDetectNumber(String bottleDetectNumber) {
		this.bottleDetectNumber = bottleDetectNumber;
	}
	public String getOperatorName() {
		return operatorName;
	}
	public void setOperatorName(String operatorName) {
		this.operatorName = operatorName;
	}
	public String getBottleValveTestNo() {
		return bottleValveTestNo;
	}
	public void setBottleValveTestNo(String bottleValveTestNo) {
		this.bottleValveTestNo = bottleValveTestNo;
	}
	public String getBottleValveType() {
		return bottleValveType;
	}
	public void setBottleValveType(String bottleValveType) {
		this.bottleValveType = bottleValveType;
	}
	public String getBottleValveTestPressure() {
		return bottleValveTestPressure;
	}
	public void setBottleValveTestPressure(String bottleValveTestPressure) {
		this.bottleValveTestPressure = bottleValveTestPressure;
	}
	public String getBottleValveTestMedium() {
		return bottleValveTestMedium;
	}
	public void setBottleValveTestMedium(String bottleValveTestMedium) {
		this.bottleValveTestMedium = bottleValveTestMedium;
	}
	public String getBottleValveChangeResult() {
		return bottleValveChangeResult;
	}
	public void setBottleValveChangeResult(String bottleValveChangeResult) {
		this.bottleValveChangeResult = bottleValveChangeResult;
	}
	
}
